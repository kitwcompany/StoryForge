//! 雪花写作法 (Snowflake Method) 提示词模板
//!
//! 从一句话到完整小说的渐进细化方法论。

/// 获取雪花法第 N 步的系统提示词
pub fn get_step_prompt(step: usize) -> &'static str {
    match step {
        1 => SNOWFLAKE_STEP_01,
        2 => SNOWFLAKE_STEP_02,
        3 => SNOWFLAKE_STEP_03,
        4 => SNOWFLAKE_STEP_04,
        5 => SNOWFLAKE_STEP_05,
        6 => SNOWFLAKE_STEP_06,
        7 => SNOWFLAKE_STEP_07,
        8 => SNOWFLAKE_STEP_08,
        9 => SNOWFLAKE_STEP_09,
        10 => SNOWFLAKE_STEP_10,
        _ => SNOWFLAKE_STEP_01,
    }
}

pub const SNOWFLAKE_STEP_01: &str = r#"【雪花法第1步：一句话故事】
请用一句话概括这个故事（15字以内）。
格式：主角 + 目标 + 障碍（含情感冲突）。
示例：一位失忆的剑客在寻找自己过去的过程中，被迫面对曾经背叛的挚友。"#;

pub const SNOWFLAKE_STEP_02: &str = r#"【雪花法第2步：一段式扩展】
将以下一句话故事扩展为五句话的段落：
1. 交代背景和主角（含一个灾难性事件）
2. 第一个灾难/转折（主角被迫行动）
3. 第二个灾难（情况更糟，引入新角色）
4. 第三个灾难（主角濒临绝境）
5. 结局（高潮后的收场，含情感回响）

一句话故事：{{one_sentence}}"#;

pub const SNOWFLAKE_STEP_03: &str = r#"【雪花法第3步：角色概要】
为主要角色各写一页概要，包含：
- 角色名 + 一句话故事线（从角色视角）
- 动机：TA想要什么？
- 目标：TA要达成什么？
- 冲突：什么阻止了TA？
- 顿悟：TA在故事结尾学到什么？
- 一句话总结：这个角色在故事中的作用

故事段落：{{paragraph}}"#;

pub const SNOWFLAKE_STEP_04: &str = r#"【雪花法第4步：情节扩展】
将五句故事段落中的每一句话扩展为一段（每段5-7句），形成完整的故事梗概。

原段落：{{paragraph}}"#;

pub const SNOWFLAKE_STEP_05: &str = r#"【雪花法第5步：角色详细表】
为主要角色写完整人物小传（每角色1-2页）：
- 基本信息：姓名、年龄、外貌特征
- 背景故事：成长经历、创伤事件
- 性格：优势、缺陷、矛盾
- 关系网：与其他角色的关系
- 弧光：故事开始时的状态 → 结束时的转变
- 秘密：角色隐藏什么？

故事梗概：{{synopsis}}"#;

pub const SNOWFLAKE_STEP_06: &str = r#"【雪花法第6步：四页大纲】
将故事梗概扩展为四页长度的详细大纲（约2000字）：
- 补充过渡场景和次要情节
- 明确每一场景的POV角色
- 标注情感节奏（紧张→舒缓→紧张）
- 确保三幕结构比例：25%-50%-25%

梗概：{{synopsis}}"#;

pub const SNOWFLAKE_STEP_07: &str = r#"【雪花法第7步：角色视角表】
为每个主要角色写完整的故事线（从该角色视角重述整个故事）。
特别关注：
- 该角色何时登场？何时退场？
- 该角色在每幕中的情感变化
- 该角色与其他角色的关键互动场景

四页大纲：{{outline}}"#;

pub const SNOWFLAKE_STEP_08: &str = r#"【雪花法第8步：场景清单】
将四页大纲拆分为场景清单，每个场景包含：
- 场景编号 + 场景标题
- POV角色
- 戏剧目标：这场景要完成什么？
- 冲突：谁反对这个目标？
- 挫折/转折：结果如何？
- 情感转变：角色进入和离开时的情绪变化

大纲：{{outline}}"#;

pub const SNOWFLAKE_STEP_09: &str = r#"【雪花法第9步：场景扩展】
将每个场景扩展为一段描述（3-5句）：
- 场景地点和时间
- 登场角色
- 场景内发生的核心事件
- 场景如何推动整体情节
- 场景结尾的悬念或钩子

场景清单：{{scene_list}}"#;

pub const SNOWFLAKE_STEP_10: &str = r#"【雪花法第10步：初稿写作】
现在开始正式写作。根据场景扩展，逐场景撰写初稿。
写作原则：
- 不要回头修改，先完成再完善
- 每场景聚焦于一个戏剧目标
- 保持POV一致性
- 让对话推动情节
- 每章结尾留下悬念

当前场景：{{scene_description}}"#;
